package week2;

public class DataType3 {

	public static void main(String[] args) {
		byte  bData = 65; //1byte=8bit -128~127까지
		char  cData =90; //char은 -가 없다
		float  fData =3.14f; //반드시 뒤에 f를 붙여야한다 아니면 double이 붙는다
		long  lData =10000000000L; //L
	    boolean boolD=true;//bool은 true
		System.out.println("bData = "+ bData);
		System.out.println("cData = "+ cData);
		System.out.println("fData = "+ fData);
		System.out.println("lData = "+ lData);
		System.out.println("boolean = "+ boolD);

	}

}
