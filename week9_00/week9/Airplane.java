// Airplane 클래스 선언 - 비행기의 기본 동작을 정의
public class Airplane {
    // 착륙을 나타내는 메서드
    public void land() {
        System.out.println("착륙합니다"); // "착륙합니다" 출력
    }

    // 비행을 나타내는 메서드
    public void fly() {
        System.out.println("일반비행입니다"); // "일반비행입니다" 출력
    }

    // 이륙을 나타내는 메서드
    public void takeoff() {
        System.out.println("이륙합니다"); // "이륙합니다" 출력
    }
}
